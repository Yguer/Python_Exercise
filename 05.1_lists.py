#Data Lists
names = ["Naruto", "Sakura", "Hitachi", "Ero Senin"]
print(type(names))

#Replacing an element from the list:
names[1] = "Kobzeva"
print(names)

#Picking values from the list:
print(names[0:2])
print(names[0:3])
print(names[1:3])

#Picking using RANGE function:
rango = list(range(0,10))
print(rango[0:2])